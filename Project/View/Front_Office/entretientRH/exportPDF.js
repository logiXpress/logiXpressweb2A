async function exportPDF() {
    const { jsPDF } = window.jspdf;
    const doc = new jsPDF({
      orientation: 'portrait',
      unit: 'mm',
      format: 'a4'
    });
  

    doc.setFont('helvetica', 'normal');
    doc.setFontSize(12);
  
    // Header
    doc.setFontSize(18);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(46, 204, 113); 
    doc.text('Candidate List', 20, 20);
    
    
    doc.addImage('../../../Public/assets/img/logo.png', 'PNG', 160, 15, 40, 10);
    
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text('Generated on: ' + new Date().toLocaleDateString(), 20, 28);
  
    // Table data
    const table = document.querySelector('#datatables');
    const headers = Array.from(table.querySelectorAll('thead th')).slice(0, -1);
    const rows = Array.from(table.querySelectorAll('tbody tr'));
  
    const head = [headers.map(th => th.innerText.trim())];
    const body = rows.map(tr => {
      const cells = Array.from(tr.querySelectorAll('td')).slice(0, -1);
      return cells.map(td => td.innerText.trim());
    });
  
    
    doc.autoTable({
      head: head,
      body: body,
      startY: 35,
      theme: 'striped',
      headStyles: {
        fillColor: [46, 204, 113], 
        textColor: [255, 255, 255], 
        fontSize: 9,
        fontStyle: 'bold'
      },
      bodyStyles: {
        fontSize: 10,
        textColor: [50, 50, 50]
      },
      alternateRowStyles: {
        fillColor: [240, 240, 240] 
      },
      margin: { top: 35, left: 20, right: 20, bottom: 20 },
      styles: {
        lineColor: [200, 200, 200],
        lineWidth: 0.1
      }
    });
  
    // Footer
    const pageCount = doc.internal.getNumberOfPages();
    for (let i = 1; i <= pageCount; i++) {
      doc.setPage(i);
      doc.setFontSize(8);
      doc.setTextColor(100, 100, 100);
      doc.text(
        `Page ${i} of ${pageCount} | Generated by LogiXpress`,
        doc.internal.pageSize.width / 2,
        doc.internal.pageSize.height - 10,
        { align: 'center' }
      );
    }
  
    
    doc.save('candidates.pdf');
  }



